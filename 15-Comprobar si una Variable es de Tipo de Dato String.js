// Comprobar si una Variable es de Tipo de Dato String.

let a = 'JavaScript es genial';
let b = 'ES6';
let c = new Date();
let d = 5;

if(typeof a === 'string'){
    console.log('El tipo de dato de `a` es string');
}else{
    console.log('El tipo de dato de `a` no es string');
}

if(typeof b === 'string'){
    console.log('El tipo de dato de `b` es string');
}else{
    console.log('El tipo de dato de `b` no  es string');
}

if(typeof c === 'string'){
    console.log('El tipo de dato de `c` es string');
}else{
    console.log('El tipo de dato de `c`  no es string');
}

if(typeof d === 'string'){
    console.log('El tipo de dato de `d` es string');
}else{
    console.log('El tipo de dato de `d`  no es string');
}